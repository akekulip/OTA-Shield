"""extract_paper_numbers.py — turn measured results into LaTeX snippets.

The paper MUST NOT contain hardcoded numbers. This script reads everything
in `runs/experiments/_agg/` plus `runs/baseline_thresholds.json` (E7) plus
`runs/throughput/results.csv` (E11) plus `runs/baseline_suricata/comparison.json`
(E10), and emits a single `paper/numbers.tex` file containing
`\newcommand{\<key>}{<value>}` definitions the LaTeX source can `\input{}`.

Usage:
    python3 experiments/extract_paper_numbers.py \\
        --agg-dir runs/experiments/_agg \\
        --out paper/numbers.tex

The paper writes e.g. `\OTAShieldEoneFone{}` instead of `1.000`. If we
re-run experiments and numbers change, the paper updates automatically.
Anything missing produces `??` in the output (visible in the rendered PDF
so we KNOW we have a missing measurement, not a silent default).
"""
from __future__ import annotations
import argparse, json, math, sys
from pathlib import Path

# T2.11 — experiments whose Table-6 row is "deterministic" (zero-variance
# hardware acceptance test, single-cell or fixed-N counts) should not emit
# bootstrap CIlo/CIhi macros at all. Listing them here makes the
# suppression auditable: any future paper text that tries to render
# `\E<id>...CIlo` for one of these IDs will produce `??` and be visible
# in the rendered PDF. Source: MASTER_LEDGER §2 T2.11.
DETERMINISTIC_EXP_IDS = frozenset({
    "EOne",          # E1
    "ETwo",          # E2
    "EFour",         # E4
    "ESix",          # E6
    "ENine",         # E9
    "EThirteen",     # E13
    "EEighteen",     # E18
    "ETwentyTwo",    # E22
})


def fmt(x, prec: int = 3, default: str = "??",
        nan_default: str = "\\ensuremath{\\text{N/A}}") -> str:
    """Format a measured number for paper inclusion.

    - Returns `default` (`??`, visible in PDF) if the value is genuinely
      missing (None, non-numeric) — author MUST notice.
    - Returns `nan_default` (`N/A`) if the value is NaN — the experiment
      ran but the metric is mathematically undefined (e.g. precision with
      tp+fp = 0, recall with tp+fn = 0). These are legitimate outcomes,
      not missing measurements.
    """
    if x is None:
        return default
    try:
        v = float(x)
    except (TypeError, ValueError):
        return default
    if math.isnan(v):
        return nan_default
    return f"{v:.{prec}f}"


def fmt_int(x, default: str = "??") -> str:
    if x is None:
        return default
    try:
        return str(int(x))
    except (TypeError, ValueError):
        return default


def emit_metric(out: list[str], key: str, value: str,
                comment: str = "") -> None:
    """Write `\newcommand{\KEY}{VALUE}% comment`."""
    line = "\\newcommand{\\" + key + "}{" + value + "}"
    if comment:
        line += "  % " + comment
    out.append(line)


def safe_load(p: Path) -> dict | None:
    if not p.exists():
        return None
    try:
        return json.loads(p.read_text())
    except json.JSONDecodeError:
        return None


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--agg-dir", default="runs/experiments/_agg",
                    type=Path)
    ap.add_argument("--baseline-json",
                    default="runs/baseline_thresholds.json", type=Path)
    ap.add_argument("--throughput-csv",
                    default="runs/throughput/results.csv", type=Path)
    ap.add_argument("--suricata-json",
                    default="runs/baseline_suricata/comparison.json",
                    type=Path)
    ap.add_argument("--suricata-stateful-json",
                    default="runs/baseline_suricata_stateful/comparison.json",
                    type=Path)
    ap.add_argument("--suricata-permissive-json",
                    default=("runs/baseline_suricata_stateful_permissive/"
                             "comparison.json"),
                    type=Path)
    ap.add_argument("--zeek-json",
                    default="runs/baseline_zeek/comparison.json",
                    type=Path)
    ap.add_argument("--suricata-rat-json",
                    default=("runs/baseline_suricata/"
                             "suricata_rat_comparison.json"),
                    type=Path)
    ap.add_argument("--suricata-perm-rat-json",
                    default=("runs/baseline_suricata_stateful_permissive/"
                             "suricata_rat_perm_comparison.json"),
                    type=Path)
    ap.add_argument("--out", default="paper/numbers.tex", type=Path)
    args = ap.parse_args()

    out = [
        "% AUTO-GENERATED by extract_paper_numbers.py — DO NOT EDIT BY HAND.",
        "% Every paper number traces to a measured run. Missing values appear",
        "% as `??` in the PDF so the author cannot accidentally publish with",
        "% an unmeasured claim.",
        "",
    ]

    # LaTeX control sequences must be pure letters — no digits allowed
    # (a digit after `\E` is parsed as a parameter reference). Map digits
    # to word equivalents so every generated \newcommand is valid.
    digit_words = {
        "0": "Zero",  "1": "One",   "2": "Two",   "3": "Three",
        "4": "Four",  "5": "Five",  "6": "Six",   "7": "Seven",
        "8": "Eight", "9": "Nine",
    }

    def tex_safe(name: str) -> str:
        out = []
        for c in name:
            if c.isalpha():
                out.append(c)
            elif c in digit_words:
                out.append(digit_words[c])
        return "".join(out)

    # ---- Per-experiment aggregates ----
    for path in sorted(args.agg_dir.glob("*.json") if args.agg_dir.exists()
                        else []):
        data_all = json.loads(path.read_text())
        agg = data_all.get("aggregate", {})
        eid = tex_safe(path.stem)
        # Experiments without a standard `aggregate` block (e.g. E17 uses
        # `strategy_breakdown`) cannot populate precision/recall/F1. Emit
        # N/A for the precision/recall commands — BUT still fall through
        # to the scenario_outcomes / strategy_breakdown readers below so
        # the non-classifier experiments get their own specific metrics.
        if not agg:
            for suf in ("Precision", "Recall", "FOne", "Accuracy"):
                for tag in ("Mean", "CIlo", "CIhi"):
                    emit_metric(out, f"{eid}{suf}{tag}",
                                "\\ensuremath{\\text{N/A}}",
                                f"{path.stem} {suf} {tag} (non-standard schema)")
            emit_metric(out, f"{eid}NTrials",
                        "\\ensuremath{\\text{N/A}}",
                        f"{path.stem} n trials (non-standard schema)")
        # metric JSON key → LaTeX command suffix (no digits allowed in
        # control sequences).
        metric_map = {"precision": "Precision", "recall": "Recall",
                       "f1": "FOne", "accuracy": "Accuracy"}
        if not agg:
            metric_map = {}
        for metric, tex_suffix in metric_map.items():
            block = agg.get(metric, {})
            mean = block.get("mean")
            lo, hi = block.get("ci_lo"), block.get("ci_hi")
            # If CI is NaN but mean is well-defined (zero-variance case),
            # emit the mean as both CI bounds so the rendered interval
            # reads e.g. "[1.000, 1.000]" rather than "[??, ??]". The
            # zero-variance nature is discussed in-text.
            if mean is not None and not (isinstance(mean, float) and math.isnan(mean)):
                if lo is None or (isinstance(lo, float) and math.isnan(lo)):
                    lo = mean
                if hi is None or (isinstance(hi, float) and math.isnan(hi)):
                    hi = mean
            emit_metric(out, f"{eid}{tex_suffix}Mean",
                        fmt(mean),
                        f"{path.stem} {metric} mean")
            # T2.11: Skip bootstrap CI macros for deterministic experiments.
            # Prefix match must terminate at a non-letter (lowercase)
            # boundary so that "EOne" (E1) does not also match "EOneTwo"
            # (E12) or "EOneNine" (E19) — those are stress/stochastic
            # rows that legitimately carry CIs.
            is_deterministic = False
            for prefix in DETERMINISTIC_EXP_IDS:
                if eid == prefix or (eid.startswith(prefix)
                                      and len(eid) > len(prefix)
                                      and not eid[len(prefix)].isupper()):
                    is_deterministic = True
                    break
            if not is_deterministic:
                emit_metric(out, f"{eid}{tex_suffix}CIlo",
                            fmt(lo), "95% CI lower")
                emit_metric(out, f"{eid}{tex_suffix}CIhi",
                            fmt(hi), "95% CI upper")
        if agg:
            nt = data_all.get("n_trials")
            emit_metric(out, f"{eid}NTrials", fmt_int(nt), "n trials")

        # Total event count across all trials (tp+tn+fp+fn). Useful for
        # prose like "0 FP across \EsevenlongbaselineEventTotal events"
        # — especially for pure-LEGIT experiments like E7 where the
        # evaluation-corpus size is a headline number.
        ptr = data_all.get("per_trial_raw") or []
        if ptr:
            ev_total = sum(int(t.get("tp", 0)) + int(t.get("tn", 0)) +
                           int(t.get("fp", 0)) + int(t.get("fn", 0))
                           for t in ptr)
            ev_tn = sum(int(t.get("tn", 0)) for t in ptr)
            ev_fp = sum(int(t.get("fp", 0)) for t in ptr)
            emit_metric(out, f"{eid}EventTotal", fmt_int(ev_total),
                        "total decisions across all trials")
            emit_metric(out, f"{eid}EventTN", fmt_int(ev_tn),
                        "TN events (benign, correctly passed)")
            emit_metric(out, f"{eid}EventFP", fmt_int(ev_fp),
                        "FP events (benign, incorrectly flagged)")
        # Latency block.
        #
        # T1.3 fix: the aggregator stores `latency_seconds.median` =
        # stage~1 median (send -> digest arrival), not end-to-end. The
        # paper-side macros must encode end-to-end = stage~1 + stage~2
        # (digest arrival -> controller decision) so the abstract and
        # §4.7 prose are arithmetically consistent. Stage~2 medians are
        # written by `experiments/latency_stages_e14.py` to
        # `runs/latency_stages/summary.csv`.
        lat = data_all.get("latency_seconds", {}) or {}
        if lat:
            stage1_med_s = lat.get("median")
            stage1_p95_s = lat.get("p95")
            stage2_med_ms = 0.0
            stage2_p95_ms = 0.0
            stage2_csv = Path("runs/latency_stages/summary.csv")
            if eid == "EEightstochastic" and stage2_csv.exists():
                try:
                    for line in stage2_csv.read_text().splitlines()[1:]:
                        parts = [p.strip() for p in line.split(",")]
                        if not parts or len(parts) < 4:
                            continue
                        if parts[0].startswith("stage2"):
                            stage2_med_ms = float(parts[2])
                            stage2_p95_ms = float(parts[3])
                            break
                except (ValueError, IndexError):
                    stage2_med_ms = 0.0
                    stage2_p95_ms = 0.0
            end_to_end_med_ms = (
                (stage1_med_s * 1000.0 + stage2_med_ms)
                if stage1_med_s is not None else None
            )
            end_to_end_p95_ms = (
                (stage1_p95_s * 1000.0 + stage2_p95_ms)
                if stage1_p95_s is not None else None
            )
            emit_metric(out, f"{eid}LatencyMedianMs",
                        fmt(end_to_end_med_ms, 1)
                        if end_to_end_med_ms is not None else "??",
                        "latency median ms (end-to-end = stage1+stage2)")
            emit_metric(out, f"{eid}LatencyPninefiveMs",
                        fmt(end_to_end_p95_ms, 1)
                        if end_to_end_p95_ms is not None else "??",
                        "latency p95 ms (end-to-end = stage1+stage2)")
            emit_metric(out, f"{eid}LatencyN", fmt_int(lat.get("n")),
                        "latency sample count")

        # E12-style: scenario_outcomes block with PASS / HOLD-only /
        # DROP / ND per benign sub-scenario. The precision/recall view
        # is misleading for pure-LEGIT experiments because every rule-
        # fire registers as FP even when §6a correctly demotes it. The
        # operational metric is "0 DROP on benign, 100% PASS final".
        so = data_all.get("scenario_outcomes") or {}
        # Fallback: if the aggregator did not emit scenario_outcomes, but
        # the experiment is a pure-benign run (E12 "benign rollout"
        # class — all events should end as TN), synthesize an outcomes
        # summary from per_trial_raw + per_rule_counts so the paper
        # prose still renders 450 events / 100% PASS instead of 0/0.
        # "Silent PASS" = TN events where no rule fired (rule-key "-").
        # "HOLD-only"   = TN events where rules fired but §6a demoted.
        if not so and eid in {"EOneTwobenignrollout"}:
            ptr = data_all.get("per_trial_raw") or []
            prc = data_all.get("per_rule_counts") or {}
            total_tn = sum(int(t.get("tn", 0)) for t in ptr)
            total_fp = sum(int(t.get("fp", 0)) for t in ptr)
            silent_pass = int((prc.get("-") or {}).get("TN", 0))
            hold_only = max(0, total_tn - silent_pass)
            # For a pure-benign run, drop == fp (any non-TN on legit =
            # false drop). no_decision is recorded separately.
            so = {
                "_aggregate_from_raw": {
                    "pass": silent_pass,
                    "hold_only": hold_only,
                    "drop": total_fp,
                    "no_decision": sum(int(t.get("no_decision", 0))
                                       for t in ptr),
                }
            }
        if so:
            total_pass = sum(int(v.get("pass", 0)) for v in so.values())
            total_hold = sum(int(v.get("hold_only", 0)) for v in so.values())
            total_drop = sum(int(v.get("drop", 0)) for v in so.values())
            total_nd = sum(int(v.get("no_decision", 0)) for v in so.values())
            total = total_pass + total_hold + total_drop + total_nd
            emit_metric(out, f"{eid}ScenarioTotal", fmt_int(total),
                        "scenario events total")
            emit_metric(out, f"{eid}ScenarioPass", fmt_int(total_pass),
                        "PASS events (no rules fired)")
            emit_metric(out, f"{eid}ScenarioHoldOnly", fmt_int(total_hold),
                        "HOLD-only events (rules fired but §6a demoted to PASS)")
            emit_metric(out, f"{eid}ScenarioDrop", fmt_int(total_drop),
                        "DROP events")
            emit_metric(out, f"{eid}ScenarioND", fmt_int(total_nd),
                        "no-decision events")
            if total:
                emit_metric(out, f"{eid}ScenarioFinalPassPct",
                            fmt(100 * (total_pass + total_hold) / total, 2),
                            "final PASS rate (pass + hold-only)")
                emit_metric(out, f"{eid}ScenarioDropPct",
                            fmt(100 * total_drop / total, 2),
                            "DROP rate on benign")
                emit_metric(out, f"{eid}ScenarioNDPct",
                            fmt(100 * total_nd / total, 2),
                            "no-decision rate")

            # Per-scenario macros for the §6b paper narrative. One
            # emit block per benign sub-scenario; key is the CamelCase
            # form of the scenario name (e.g. benign_authorized_rollback
            # -> BenignAuthorizedRollback). Dedicated aliases for the
            # §6b authorized rollback and its preamble so prose can cite
            # them without reconstructing the scenario name.
            def _camel(s: str) -> str:
                return "".join(p.capitalize() for p in s.split("_"))
            for scn_name, scn in so.items():
                # tex_safe strips digits (LaTeX forbids digits in
                # control sequences); e.g. benign_migration_src1 ->
                # BenignMigrationSrcOne.
                suffix = tex_safe(_camel(scn_name))
                p = int(scn.get("pass", 0))
                h = int(scn.get("hold_only", 0))
                d = int(scn.get("drop", 0))
                nd = int(scn.get("no_decision", 0))
                n = p + h + d + nd
                emit_metric(out, f"{eid}{suffix}N", fmt_int(n),
                            f"{scn_name} events total")
                emit_metric(out, f"{eid}{suffix}Pass", fmt_int(p),
                            f"{scn_name} PASS events")
                emit_metric(out, f"{eid}{suffix}HoldOnly", fmt_int(h),
                            f"{scn_name} HOLD-only events")
                emit_metric(out, f"{eid}{suffix}Drop", fmt_int(d),
                            f"{scn_name} DROP events")

            # §6b-specific aliases. The paper cites these by name; the
            # macros resolve to 0 when the scenario is absent (e.g. an
            # older aggregate without §6b wiring).
            ar = so.get("benign_authorized_rollback") or {}
            rp = so.get("benign_rollback_preamble") or {}
            emit_metric(out, "OTAShieldAuthorizedRollbackEvents",
                        fmt_int(sum(int(ar.get(k, 0)) for k in
                                    ("pass", "hold_only", "drop",
                                     "no_decision"))),
                        "§6b authorized rollback scenario events")
            emit_metric(out, "OTAShieldAuthorizedRollbackDrops",
                        fmt_int(int(ar.get("drop", 0))),
                        "§6b authorized rollback DROP count")
            emit_metric(out, "OTAShieldRollbackPreambleEvents",
                        fmt_int(sum(int(rp.get(k, 0)) for k in
                                    ("pass", "hold_only", "drop",
                                     "no_decision"))),
                        "§6b rollback preamble events (v48 seeding)")

        # E17-style: strategy_breakdown (per mimicry strategy). Emits
        # catch-rate per strategy and the worst-case strategy name so
        # the paper can cite both the aggregate and the weakest point.
        sb = data_all.get("strategy_breakdown") or {}
        if sb:
            total_caught = sum(int(v.get("caught", 0)) for v in sb.values())
            total_missed = sum(int(v.get("missed", 0)) for v in sb.values())
            total_ev = total_caught + total_missed
            emit_metric(out, f"{eid}StrategyTotal", fmt_int(total_ev),
                        "mimicry strategy events total")
            emit_metric(out, f"{eid}StrategyCaught", fmt_int(total_caught),
                        "events caught")
            emit_metric(out, f"{eid}StrategyMissed", fmt_int(total_missed),
                        "events missed")
            if total_ev:
                emit_metric(out, f"{eid}StrategyCatchPct",
                            fmt(100 * total_caught / total_ev, 1),
                            "overall catch rate %")
            # Worst strategy (lowest catch rate).
            def _rate(v: dict) -> float:
                n = int(v.get("n_events", 0))
                c = int(v.get("caught", 0))
                return c / n if n else 1.0
            worst_name, worst_v = min(sb.items(), key=lambda kv: _rate(kv[1]))
            wn = int(worst_v.get("n_events", 0))
            wc = int(worst_v.get("caught", 0))
            emit_metric(out, f"{eid}StrategyWorstName",
                        tex_safe(worst_name),
                        f"worst-catch strategy ({worst_name})")
            emit_metric(out, f"{eid}StrategyWorstN", fmt_int(wn),
                        "worst-strategy events")
            emit_metric(out, f"{eid}StrategyWorstCaught", fmt_int(wc),
                        "worst-strategy caught")
            emit_metric(out, f"{eid}StrategyWorstCatchPct",
                        fmt(100 * wc / max(wn, 1), 1),
                        "worst-strategy catch rate %")

    # ---- E19 ablation (R6 removed) ----
    # R6's unique contribution is measured by comparing detection rate
    # between the main (R6 on) and ablation (R6 off) runs. Ablation runs
    # live in a separate tree; aggregate them here on the fly.
    abl_dir = Path("runs/experiments_ablation/E19_rollback")
    if abl_dir.exists():
        out.append("")
        out.append("% --- E19 R6 ablation (R6 disabled in controller) ---")
        total_rollback = 0
        total_drop = 0
        trials = 0
        for t in sorted(p for p in abl_dir.iterdir() if p.is_dir()):
            dec_path = t / "controller_decisions.jsonl"
            gt_path = t / "ground_truth.json"
            if not dec_path.exists() or not gt_path.exists():
                continue
            trials += 1
            gt = json.loads(gt_path.read_text())
            n_rb = sum(1 for e in gt.get("events", [])
                       if "rollback" in (e.get("scenario", "") or ""))
            total_rollback += n_rb
            for line in dec_path.read_text().splitlines():
                line = line.strip()
                if not line:
                    continue
                try:
                    d = json.loads(line)
                except json.JSONDecodeError:
                    continue
                if (d.get("decision") or "").upper() == "DROP":
                    total_drop += 1
        emit_metric(out, "EnineteenAblationNTrials", fmt_int(trials),
                    "ablation trials")
        emit_metric(out, "EnineteenAblationRollbackEvents",
                    fmt_int(total_rollback),
                    "rollback ground-truth events across ablation trials")
        emit_metric(out, "EnineteenAblationDrops", fmt_int(total_drop),
                    "DROPs produced when R6 is disabled")
        det = (total_drop / total_rollback) if total_rollback else 0.0
        emit_metric(out, "EnineteenAblationDetectionPct",
                    fmt(100 * det, 1),
                    "detection rate % without R6")

    # ---- E7 empirical thresholds ----
    bl = safe_load(args.baseline_json)
    if bl:
        out.append("")
        out.append("% --- E7 empirical baseline-derived thresholds ---")
        emit_metric(out, "EsevenNEvents",
                    fmt_int(bl.get("n_events")), "E7 baseline events")
        emit_metric(out, "EsevenDurationS",
                    fmt(bl.get("duration_s"), 1), "E7 baseline duration s")
        bms = bl.get("distinct_bms_per_window", {}) or {}
        emit_metric(out, "EsevenDistinctBMSPninenine",
                    fmt(bms.get("p99"), 1),
                    "P99 distinct BMSes per 60s window")
        emit_metric(out, "EsevenDistinctBMSPninenineNine",
                    fmt(bms.get("p99_9"), 1),
                    "P99.9 distinct BMSes per 60s window")
        emit_metric(out, "EsevenDistinctBMSMax",
                    fmt_int(bms.get("max")),
                    "Max distinct BMSes per 60s window")
        iv = bl.get("per_bms_interval_s", {}) or {}
        emit_metric(out, "EsevenIntervalPzeroOne",
                    fmt(iv.get("p0_1"), 1),
                    "P0.1 per-BMS interval (s)")
        emit_metric(out, "EsevenIntervalPone",
                    fmt(iv.get("p1"), 1),
                    "P1 per-BMS interval (s)")
        sz = bl.get("firmware_size_bytes", {}) or {}
        emit_metric(out, "EsevenSizePninenineNine",
                    fmt_int(sz.get("p99_9")),
                    "P99.9 firmware size B")
        rec = bl.get("threshold_recommendations", {}) or {}
        emit_metric(out, "EsevenRfiveRecommended",
                    fmt_int(rec.get("R5_count")),
                    "R5 threshold recommended from data")
        emit_metric(out, "EsevenRoneRecommended",
                    fmt_int(rec.get("R1_min_s")),
                    "R1 minimum interval recommended s")
        emit_metric(out, "EsevenRfourRecommended",
                    fmt_int(rec.get("R4_max_B")),
                    "R4 max bytes recommended")

    # ---- E11 throughput stress ----
    # CSV format (new): target_pps,sent,observed,loss_pct
    if args.throughput_csv.exists():
        out.append("")
        out.append("% --- E11 throughput stress ---")
        rows = args.throughput_csv.read_text().strip().splitlines()[1:]
        max_loss_pct = 0.0
        max_offered = 0
        total_sent = 0
        total_obs = 0
        for r in rows:
            parts = r.split(",")
            if len(parts) >= 4:
                try:
                    offered = int(parts[0])
                    sent = int(parts[1])
                    obs = int(parts[2])
                    loss = float(parts[3])
                except ValueError:
                    continue
                total_sent += sent
                total_obs += obs
                if loss < 1.0 and offered > max_offered:
                    max_offered = offered
                if loss > max_loss_pct:
                    max_loss_pct = loss
        emit_metric(out, "ElevenMaxOfferedNoLoss",
                    fmt_int(max_offered),
                    "max offered pps with <1% loss")
        emit_metric(out, "ElevenWorstLossPct",
                    fmt(max_loss_pct, 2),
                    "worst-case digest loss %")
        emit_metric(out, "ElevenTotalSent", fmt_int(total_sent),
                    "total packets sent")
        emit_metric(out, "ElevenTotalObserved", fmt_int(total_obs),
                    "total digests observed")
        agg_loss = (1 - total_obs / total_sent) * 100 if total_sent else 0
        emit_metric(out, "ElevenAggregateLossPct", fmt(agg_loss, 2),
                    "aggregate digest loss %")

    # ---- E10 Suricata baseline ----
    sur = safe_load(args.suricata_json)
    if sur:
        out.append("")
        out.append("% --- E10 Suricata baseline comparison ---")
        # Precision is mathematically undefined when tp+fp = 0 (no
        # predicted-positives). suricata_baseline.py writes `null` in
        # that case; render as N/A, not as a missing measurement.
        prec = sur.get("precision")
        if prec is None:
            prec_str = "\\ensuremath{\\text{N/A}}"
        else:
            prec_str = fmt(prec)
        emit_metric(out, "EtenSuricataPrecision", prec_str,
                    "Suricata precision")
        emit_metric(out, "EtenSuricataRecall",
                    fmt(sur.get("recall")), "Suricata recall")
        emit_metric(out, "EtenSuricataAccuracy",
                    fmt(sur.get("accuracy")), "Suricata accuracy")
        emit_metric(out, "EtenSuricataNDecisions",
                    fmt_int(sur.get("n_decisions")), "Suricata n decisions")
        lat_block = sur.get("latency_seconds") or {}
        # Suricata alert latency is undefined when no alerts fired
        # (recall = 0 → no TP → no latency samples). Emit N/A.
        if lat_block.get("mean") is not None:
            lat_val = fmt(lat_block.get("mean") * 1000, 1)
        else:
            lat_val = "\\ensuremath{\\text{N/A}}"
        emit_metric(out, "EtenSuricataLatencyMeanMs", lat_val,
                    "Suricata latency mean ms")

    def _emit_baseline_block(prefix: str, title: str, path: Path) -> None:
        """Emit Precision/Recall/Accuracy + TP/FP/FN/TN for a baseline
        comparison.json at `path`. Silent if file is missing."""
        d = safe_load(path)
        if not d:
            return
        out.append("")
        out.append(f"% --- E10 {title} ---")
        prec = d.get("precision")
        prec_str = ("\\ensuremath{\\text{N/A}}"
                    if prec is None else fmt(prec))
        emit_metric(out, f"{prefix}Precision", prec_str,
                    f"{title} precision")
        emit_metric(out, f"{prefix}Recall",
                    fmt(d.get("recall")), f"{title} recall")
        emit_metric(out, f"{prefix}Accuracy",
                    fmt(d.get("accuracy")), f"{title} accuracy")
        emit_metric(out, f"{prefix}TP",
                    fmt_int(d.get("tp")), f"{title} TP")
        emit_metric(out, f"{prefix}FP",
                    fmt_int(d.get("fp")), f"{title} FP")
        emit_metric(out, f"{prefix}FN",
                    fmt_int(d.get("fn")), f"{title} FN")
        emit_metric(out, f"{prefix}TN",
                    fmt_int(d.get("tn")), f"{title} TN")

    _emit_baseline_block("EtenSuricataStateful",
                         "Suricata stateful baseline",
                         args.suricata_stateful_json)
    _emit_baseline_block("EtenSuricataPerm",
                         "Suricata stateful-permissive baseline",
                         args.suricata_permissive_json)
    _emit_baseline_block("EtenZeek",
                         "Zeek domain-aware baseline",
                         args.zeek_json)
    _emit_baseline_block("EtenSuricataRat",
                         "Suricata+RAT apples-to-apples (M7)",
                         args.suricata_rat_json)
    _emit_baseline_block("EtenSuricataPermRat",
                         "Suricata stateful-permissive + RAT (variant vi)",
                         args.suricata_perm_rat_json)

    args.out.parent.mkdir(parents=True, exist_ok=True)
    args.out.write_text("\n".join(out) + "\n")
    n_cmds = sum(1 for l in out if l.startswith("\\newcommand"))
    print(f"Wrote {args.out} ({n_cmds} \\newcommand entries)")
